    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try{
            DAOUsuario usDAO= new DAOUsuarioImpl();
            Usuario us;
            String nombre=txtNombre.getText().trim();
            String apellido=txtApellido.getText().trim();
            String user=txtUser.getText().trim();
            String correo=txtCorreo.getText().trim();
            char[] arrayC = txtPass.getPassword();
            String pass= new String(arrayC).trim();
            String msj="";
            if(nombre.equals("")) msj+="Falta ingresar nombre\n";
            if(apellido.equals("")) msj+="Falta ingresar apellido\n";
            if(user.equals("")) msj+="Falta ingresar user\n";
            if(pass.equals("")) msj+="Falta ingresar contrase침a\n";
            if(correo.equals("")) msj+="Falta ingresar correo\n";
            if(msj.equals("")){
                us=new Usuario(nombre, apellido, user, pass, correo);
                if(usDAO.existeUsuario(us)){
                    JOptionPane.showMessageDialog(null, "EL user ya est치 siendo usado, ingrese uno diferentes","Nuevo Usuario",JOptionPane.INFORMATION_MESSAGE);
                }else{
                    if(usDAO.registrar(us)){
                        JOptionPane.showMessageDialog(null, "Usario registrado","Nuevo Usuario",JOptionPane.INFORMATION_MESSAGE);
                        dispose();
                    }else{
                        JOptionPane.showMessageDialog(null, "No se registr칩 usuario","Nuevo Usuario",JOptionPane.WARNING_MESSAGE);
                    }
                }
            }else{
                JOptionPane.showMessageDialog(null, msj,"Nuevo Usuario",JOptionPane.WARNING_MESSAGE);
            }
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Error","Nuevo Usuario",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


