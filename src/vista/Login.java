    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        try{
            String user=txtUsuario.getText();
            char[] arrayC = txtPassword.getPassword();
            String pass= new String(arrayC);
            DAOUsuario us= new DAOUsuarioImpl();
            
            if(user.equals("") || pass.equals("")){
                JOptionPane.showMessageDialog(null, "Falta ingresr usuario y/contraseña","Login",JOptionPane.WARNING_MESSAGE);
            }else{
                Usuario usuario=us.login(user, pass);
                if(usuario!=null){
                    //ingresar
                    this.setVisible(false);
                    txtUsuario.setText("");
                    txtPassword.setText("");
                    new Principal(usuario,this).setVisible(true);
                    
                }else{
                    txtUsuario.setText("");
                    txtPassword.setText("");
                    JOptionPane.showMessageDialog(null, "Usuario y/o contraseña incorrecto","Login",JOptionPane.WARNING_MESSAGE);
                    
                }
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Error inesperado","Login",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

